import { longestValidParentheses } from './1'

it.each([
    // ['', 0],
    // [')(', 0],
    // ['(()', 2],
    // ['(()()', 4],
    // ['(())', 4],
    ['()()', 4],
    ['()(()', 2],
    ['(()())', 6],
    [')()())', 4],
    ['(()()(', 4],
    ['()(())', 6],
    ['()()()', 6],
    ['(()()(', 4],
    ['))(((()', 2],
    ['()((())', 4],
    ['(()()()', 6],
    ['()(()())', 8],
    ['(()()())', 8],
    ['()((())()', 6],
    ['(())())()', 6],
    ['(()()())()', 10],
    ['))(()(((()()', 4],
    ['))(()()(()(()', 4],
])('%p => %p', (input, expected) => {
    expect(longestValidParentheses(input)).toBe(expected)
})
