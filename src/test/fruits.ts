const findIndex = (
    arr: number[],
    i: number,
    hasFound: (n: number, i: number) => boolean,
) => {
    while (i < arr.length) {
        if (hasFound(arr[i], i)) {
            return i
        }
        i += 1
    }
    return -1
}

function totalFruit(fruits: number[]): number {
    if (fruits.length < 3) {
        return fruits.length
    }
    let i1 = 0
    let i2 = findIndex(fruits, 1, (n) => n !== fruits[0])
    if (i2 === -1) {
        return fruits.length
    }

    let maxLen = i2 + 1

    let i = i2
    while (i > 0) {
        const a = fruits[i1]
        const b = fruits[i2]
        i = findIndex(fruits, i2, (n) => n !== a && n !== b)
        if (i === -1) {
            maxLen = Math.max(maxLen, fruits.length - i1)
            break
        }
        maxLen = Math.max(maxLen, i - i1)
        i1 = i2
        i2 = i
    }
    return maxLen
}

