import { candy } from './candy'

it.each([
    [[1, 0, 2], 5],
    [[1, 2, 2], 4], // 010,
    [[1, 2, 2, 1], 6], // 0110,
    [[1, 3, 2, 1], 7], // 0110,
    [[1, 30, 20, 10, 0], 11], // 03210
    [[1, 3, 2, 2, 1], 7], // 01010
])('%p => %p', (a, expected) => {
    expect(candy(a)).toEqual(expected)
})
